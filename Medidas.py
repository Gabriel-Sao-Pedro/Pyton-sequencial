a = float(input("Digite a medida A: "))
b = float(input("Digite a medida B: "))
c = float(input("Digite a medida C: "))

q = (a**2)
t = (a * b) / 2
tr = ((a + b) * c) / 2

print("Area do quadrado = {:.4f}".format(q))
print("Area do Triangulo = {:.4f}".format(t))
print("Area do trapesio = {:.4f}".format(tr))
